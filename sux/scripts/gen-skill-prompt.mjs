// Generate sux/src/skill-prompt.ts — the sux routing SKILL embedded as a string,
// so the /mcp server can serve it as an MCP prompt to every client (mobile
// included) that can't load the local plugin skill. Source of truth is
// .claude/skills/sux/SKILL.md. Run: `npm run gen:skill`.
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..", "..");
const SKILL = join(ROOT, ".claude", "skills", "sux", "SKILL.md");
const OUT = join(ROOT, "sux", "src", "skill-prompt.ts");

const raw = readFileSync(SKILL, "utf8");
// Split the YAML frontmatter (---\n…\n---) from the markdown body. The body is the
// prompt text; the frontmatter `description` becomes the prompt's description.
const m = raw.match(/^---\n([\s\S]*?)\n---\n?([\s\S]*)$/);
const front = m ? m[1] : "";
const body = (m ? m[2] : raw).trim();
const description = (front.match(/^description:\s*(.*)$/m)?.[1] ?? "sux edge-function routing guidance").trim();

const out = `// GENERATED by scripts/gen-skill-prompt.mjs (npm run gen:skill) from
// .claude/skills/sux/SKILL.md. Do not edit — edit the SKILL and regenerate.
// Embedded because a Cloudflare Worker has no runtime filesystem; the /mcp server
// serves this as the \`sux\` MCP prompt (prompts/get) so every client gets routing
// guidance even without the local plugin skill.

export const SUX_SKILL_DESCRIPTION = ${JSON.stringify(description)};

export const SUX_SKILL_PROMPT = ${JSON.stringify(body)};
`;
writeFileSync(OUT, out);
console.log(`Wrote sux/src/skill-prompt.ts — ${body.length} chars from SKILL.md.`);
